    private void searchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTNActionPerformed
        // TODO add your handling code here:
        ResultSet result;
        try{
            bookName = searchNameTF.getText();
            result = new DBSearch().searchBook(bookName);

            while(result.next()){
                vBkName.setText(bookName);
                vISBN.setText(result.getString("bookISBN"));
                vAName.setText(result.getString("authorName"));
                vCategory.setText(result.getString("category"));
                vCopies.setText(result.getString("copies"));
                byte[] photo = result.getBytes("image");
                viewIMG.setIcon(new ImageIcon(photo));
            }
        }
        catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_searchBTNActionPerformed

