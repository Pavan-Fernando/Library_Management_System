    private void upBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upBTNActionPerformed
        // TODO add your handling code here:
        String bkName = null;
        String isbn = null;
        String aName = null;
        String category = null;
        String copies = null;
        
        bkName = uBkName.getText();
        isbn = uISBN.getText();
        aName = uAName.getText();
        category = (String) uCategory.getSelectedItem();
        copies = uCopies.getText();
        
        if(bkName != null && isbn != null && aName != null && category != null && copies != null){
            BookController.BookDetailsUpdate(bookName, isbn, aName, category, copies);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please fill the all text fields", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_upBTNActionPerformed

